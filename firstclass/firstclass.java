package firstclass;

public class firstclass {

	public static void main(String[] args) {
		
		int a=20,b=40,ans;
		
		ans = a + b; 
		System.out.println("Addition => "+a+"+"+b+"="+ ans);
		ans = b - a;
		System.out.println("Subtraction => "+b+"-"+a+"="+ ans);
		ans = a * b;
		System.out.println("Multiplication => "+a+"*"+b+"="+ ans);
		ans = a / b;
		System.out.println("Division => "+a+"/"+b+"="+ ans);

	}

}
